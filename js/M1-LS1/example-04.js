const value = 27.5;

// Calculate rounded values using Math.floor(), Math.ceil(), and Math.round()
const roundedDown = Math.floor(value);
const roundedUp = Math.ceil(value);
const rounded = Math.round(value);

// Print the results to the console
console.log("Original value:", value);
console.log("Rounded down value:", roundedDown);
console.log("Rounded up value:", roundedUp);
console.log("Rounded value:", rounded);


